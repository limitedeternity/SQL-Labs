select ид_кни, жанр, цена, цена * 1.1 as [новая цена]
from Книги.dbo.книги
where жанр = 'история'
union
select ид_кни, жанр, цена, цена * 1.2 as [новая цена]
from Книги.dbo.книги
where жанр = 'психология'
union
select ид_кни, жанр, цена, цена * 1.1 as [новая цена]
from Книги.dbo.книги
where жанр not in ('история', 'психология')
order by 2 asc
