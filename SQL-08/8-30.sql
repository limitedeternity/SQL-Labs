select книги.жанр, издательства.ид_изд, count(книги.ид_кни) as [Количество книг], sum(гонорары.аванс) as [Сумма авансов]
from книги
inner join издательства on книги.издательство = издательства.ид_изд
inner join гонорары on книги.ид_кни = гонорары.ид_кни
group by книги.жанр, издательства.ид_изд
order by 1 asc
